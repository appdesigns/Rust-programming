fn main() {
    //mut - help variable to be mutable(changable)
    let mut a = 2;
    println!("The value os a is: {}", a);

    a = 3;
    println!("The value of a is: {}", a);

    // addition
    let sum = 5 + 10;
    println!("Sum is: {}", sum);

    // subtraction
    let difference = 95.5 - 4.3;
    println!("difference is: {}", difference);

    // multiplication
    let product = 4 * 30;
    println!("product is: {}", product);

    // division
    let quotient = 56.7 / 32.2;
    println!("quotient is: {}", quotient);

    // remainder
    let remainder = 43 % 5;
    println!("remainder is: {}",remainder);

    let xx =  true;
    println!("vale for xx iS: {}", xx);

    let yy:bool = false;
    println!("value for yy is: {}", yy);
}
